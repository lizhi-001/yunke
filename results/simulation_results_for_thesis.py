# ============================================================
# 便捷函数
# ============================================================

def get_type1_by_model(model: str) -> pd.DataFrame:
    """获取指定模型的 Type I error 数据"""
    return TYPE1_ERROR[TYPE1_ERROR["model"] == model].copy()

def get_power_by_model(model: str) -> pd.DataFrame:
    """获取指定模型的 Power 数据"""
    return POWER[POWER["model"] == model].copy()

def get_type1_pivot() -> pd.DataFrame:
    """Type I error 透视表：行=M，列=model"""
    return TYPE1_ERROR.pivot(index="M", columns="model", values="type1_error")[MODEL_NAMES]

def get_power_pivot() -> pd.DataFrame:
    """Power 透视表：行=delta，列=model"""
    return POWER.pivot(index="delta", columns="model", values="power")[MODEL_NAMES]

def get_power_with_actual_fro() -> pd.DataFrame:
    """Power 透视表（使用 actual_fro 作为 x 轴）"""
    return POWER.pivot(index="delta", columns="model", values=["power", "actual_fro"])


